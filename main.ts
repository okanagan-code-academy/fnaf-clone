function changePlayerState(newState: PlayerState){
    if (GameState["playerState"].indexOf(newState) < 0){
        console.log("Player State does not exists")
        return
    }
    if(currentPlayerState == newState){
        console.log("No change to Player State")
        return
    }
    currentPlayerState = newState
    console.log("Player State changed to " + newState.toString())
     
}
